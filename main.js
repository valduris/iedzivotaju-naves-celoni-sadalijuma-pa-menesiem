function randomColor() {
    var color = "#";
    for (var i = 0; i < 3; i++) {
        var randomHexString = Math.floor(Math.random() * 256).toString(16);

        if (randomHexString.length === 1) {
            randomHexString = "0" + randomHexString;
        }

        color += randomHexString;
    }
    return color;
}

function createSeries(parsedData) {
    return Object.keys(parsedData).reduce(function(memo, curr) {
        memo.push({
            label: curr,
            stroke: randomColor(),
        });
        return memo;
    }, [{}]);
}

