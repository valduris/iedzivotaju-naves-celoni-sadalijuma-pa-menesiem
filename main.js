/*
 * Return value of this function can be easily manipulated by the
 * app logic if necessary, for example, toggling which lines to show.
 */
function parseData(data) {
    return data.split(/\n/).reduce(function (memo, line) {
        var columns = line.split(";");
        var name = columns[0].replace(/\"/g, "");

        if (!memo.hasOwnProperty(name)) {
            memo[name] = [];
        }

        for (var i = 1; i <= 12; i++) {
            var raw_value = columns[i + 1];

            if (raw_value === "...") {
                continue;
            }

            var value = raw_value === "x"
                ? 0
                : raw_value.indexOf(".") === -1
                    ? parseInt(raw_value, 10)
                    : parseFloat(raw_value);

            memo[name].push(value);
        }

        return memo;
    }, {});
}

function createMonthLabels() {
    var labels = [];
    var fullYears = [2015, 2016, 2017, 2018, 2019, 2020];
    for (var i = 1; i <= 12 * fullYears.length; i++) {
        var year = fullYears[Math.floor((i - 1) / 12)];
        var month = (i % 12) > 0 ? (i % 12) : 12;

        labels.push(new Date(year, month - 1).getTime());
    }

    // Up to current month of year for available data
    for (var month = 0; month <= 7; month++) {
        labels.push(new Date(2021, month).getTime());
    }

    return labels;
}

function convertParsedDataToChartFormat(parsedData) {
    return Object.keys(parsedData).reduce(function (memo, curr, index) {
        memo.push(parsedData[curr]);
        return memo;
    }, [createMonthLabels()]);
}

